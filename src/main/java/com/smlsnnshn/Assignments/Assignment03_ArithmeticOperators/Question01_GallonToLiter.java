package com.smlsnnshn.Assignments.Assignment03_ArithmeticOperators;

public class Question01_GallonToLiter {
	
	/*
	 * Write a program to convert gallons into Liters and display it.
	 * Sample output
	 * 15 gallon is 56.7812 liter
	 */

	public static void main(String[] args) {

		int gallon = 15;
		double liter = gallon * 3.7854132;
		System.out.println(gallon + " gallon is " + liter + " liter");

	}

}
