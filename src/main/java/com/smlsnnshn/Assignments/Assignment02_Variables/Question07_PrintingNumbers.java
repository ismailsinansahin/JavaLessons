package com.smlsnnshn.Assignments.Assignment02_Variables;

public class Question07_PrintingNumbers {
	
	/*
	 * Declare and initialize an integer variable i and a floating-point 
	 * variable f.  Write a statement that writes both of their values to 
	 * console in the following format: 
	 * i=value-of-i f=value-of-f
	 * Ex:Assume i = 9 and f = 33.4
	 * The output should be:
	 * i=9 f=33.4
	 */
	
	public static void main(String[] args) {
		
		int i = 9;
		float f = 33.4f;
		System.out.println("i=" + i + " f=" + f);
		
	}

}
