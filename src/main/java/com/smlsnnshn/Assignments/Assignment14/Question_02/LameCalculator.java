package com.smlsnnshn.Assignments.Assignment14.Question_02;

/*
The LameCalculator class has instance methods that can do simple math operations 
on two numbers. The methods are: plus, minus, multiply and divide.
All the methods get two ints then the method does the required math operation and 
returns the result as an int.
*/

public class LameCalculator {	
	
	int num1;
	int num2;
	
	public int plus(int num1, int num2) {		
		return num1 + num2;
	}
	public int minus(int num1, int num2) {		
		return num1 - num2;
	}
	public int multiply(int num1, int num2) {		
		return num1 * num2;
	}
	public int divide(int num1, int num2) {		
		return num1 / num2;
	}
	
}
