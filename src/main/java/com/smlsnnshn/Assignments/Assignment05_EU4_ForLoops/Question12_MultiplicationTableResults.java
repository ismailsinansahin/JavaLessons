package com.smlsnnshn.Assignments.Assignment05_EU4_ForLoops;

public class Question12_MultiplicationTableResults {

	public static void main(String[] args) {

		for (int i=1 ; i<=10 ; i++) {
			for (int j=1 ; j<=10 ; j++) {
				System.out.print(i*j + " ");
			}
			System.out.println("");
		}
		
	}

}
